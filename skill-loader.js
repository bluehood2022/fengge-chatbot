/**
 * 解析 SKILL.md 文件，提取系统提示词
 * @param {string} skillPath - SKILL.md 文件路径
 * @returns {Object} - 包含 skill 元数据和系统提示词
 */
function loadSkill(skillPath) {
  const content = fs.readFileSync(skillPath, 'utf8');
  
  // 提取 YAML frontmatter
  const frontmatterMatch = content.match(/^---\n([\s\S]*?)\n---/);
  let metadata = {};
  
  if (frontmatterMatch) {
    const yamlContent = frontmatterMatch[1];
    // 简单解析 YAML
    metadata = parseSimpleYAML(yamlContent);
  }
  
  // 提取系统提示词部分
  // 查找所有示例和说明，构建完整的系统提示词
  const systemPrompt = buildSystemPrompt(content, metadata);
  
  return {
    name: metadata.name || 'unknown',
    version: metadata.version || '1.0.0',
    description: metadata.description || '',
    systemPrompt: systemPrompt
  };
}

/**
 * 简单解析 YAML 格式
 */
function parseSimpleYAML(yaml) {
  const result = {};
  const lines = yaml.split('\n');
  
  for (const line of lines) {
    const match = line.match(/^(\w+):\s*(.+)$/);
    if (match) {
      const key = match[1];
      let value = match[2].trim();
      
      // 处理数组
      if (value.startsWith('[') && value.endsWith(']')) {
        value = value.slice(1, -1).split(',').map(s => s.trim());
      }
      
      result[key] = value;
    }
  }
  
  return result;
}

