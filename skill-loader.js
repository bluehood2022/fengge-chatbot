/**
 * 从知识库文件加载额外语料
 * @param {string} knowledgeBasePath - 知识库目录路径
 * @returns {Object} - 分类的语料库
 */
function loadKnowledgeBase(knowledgeBasePath) {
  const categories = ['情感', '社会', '自嘲', '生活', '经典', '其他'];
  const knowledgeBase = {};
  
  for (const category of categories) {
    const filePath = path.join(knowledgeBasePath, `${category}.json`);
    try {
      if (fs.existsSync(filePath)) {
        const content = fs.readFileSync(filePath, 'utf8');
        knowledgeBase[category] = JSON.parse(content);
      }
    } catch (error) {
      console.warn(`无法加载 ${category} 知识库：`, error.message);
      knowledgeBase[category] = [];
    }
  }
  
  return knowledgeBase;
}

